    private void signUpbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpbtnActionPerformed
     System.out.println("Sign Up clicked");
    String userType, userN, password, name, query, meterNum;
    
    String sUrl = "jdbc:MYSQL://localhost:3306/ewb";
    String sUser = "root";
    String sPass = "";
    
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection(sUrl, sUser, sPass);
        Statement st = con.createStatement();
        
        // Get field values
        userType = (String) cmbUT.getSelectedItem();
        userN = txtUN.getText().trim();
        password = txtP.getText().trim();
        name = txtN.getText().trim();
        meterNum = meterN.getText().trim();

        // Validation
        if ("Select User Type".equals(userType) || userN.isEmpty() || password.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(new JFrame(), "Please fill all required fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(userN.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Please fill the Username");
            return;
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Password is empty");
            return;
        }else if(name.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Name is empty");
            return;
        }   
        
        if (!"Admin".equals(userType) && meterNum.isEmpty()) {
            JOptionPane.showMessageDialog(new JFrame(), "Please provide a Meter Number for Customers", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validate meter number in the database
            
            String query1 = "SELECT name FROM customer WHERE meter_number = ?";
            PreparedStatement pst = con.prepareStatement(query1);
            pst.setString(1, meterNum);
            ResultSet rs = pst.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(new JFrame(), "Invalid Meter Number. Please contact admin.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

        // Build query based on user type
        if ("Admin".equals(userType)) {
            query = "INSERT INTO user (userT, username, password, name) VALUES " +
                    "('" + userType + "', '" + userN + "', '" + password + "', '" + name + "')";
        } else {
            query = "INSERT INTO user (userT, username, password, name, meter_number) VALUES " +
                    "('" + userType + "', '" + userN + "', '" + password + "', '" + name + "', '" + meterNum + "')";
        }

        // Execute query
        st.execute(query);

        // Clear fields after successful insertion
        txtUN.setText("");
        txtP.setText("");
        txtN.setText("");
        meterN.setText("");

        JOptionPane.showMessageDialog(null, "Account has been created successfully!");

    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
        JOptionPane.showMessageDialog(new JFrame(), "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_signUpbtnActionPerformed

