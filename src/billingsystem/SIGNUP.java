    private void meterNFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_meterNFocusLost
        String meterNum = meterN.getText().trim();
    String sUrl = "jdbc:MYSQL://localhost:3306/ewb";
    String sUser = "root";
    String sPass = "";

    if (!meterNum.isEmpty()) {
        try {
            // Connect to the database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(sUrl, sUser, sPass);

            // Query to fetch the name based on meter number
            String query = "SELECT name FROM customer WHERE meter_number = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, meterNum);

            ResultSet rs = pst.executeQuery();

            // Check if a result is returned
            if (rs.next()) {
                String name = rs.getString("name");
                txtN.setText(name); // Set the name in the name field
            } else {
                JOptionPane.showMessageDialog(new JFrame(), "Meter number not found", "Error", JOptionPane.ERROR_MESSAGE);
                txtN.setText(""); // Clear the name field
            }

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_meterNFocusLost

